    /**
     * Metodo les permite colocar fichas siempre comprobando que el espacio este 
     * vacio, actualizando turno y comprobando si hay ganador.
     * @param box numero de la casilla presionada.
     */
    public void press(int box){
     
     if ((lb[box-1].getText().equals(""))&& state){
        
         lb[box-1].setText(file.getTypefile());
        checkwinner();
          file.ChangeShift();
         
       
     }
         if (file.getTypefile().equals("X")) {
             lbname.setText( player.getNameplayer1() + file.getTypefile());
           
         }else {
             lbname.setText( player.getNameplayer2() + file.getTypefile());
         }
         
    
    }
/**
 * el metodo comprueba si hay un ganador, mostrando el mensaje de victoria 
 * empate o borra el mensaje de turno.
 */
      public void checkwinner() {
       board = new Board();
     boolean fullsquare = true;
     
    for (int i = 0; i < board.win.length; i++) {
     if (lb[board.win[i][0]-1].getText().equals("X") &&
         lb[board.win[i][1]-1].getText().equals("X") &&
         lb[board.win[i][2]-1].getText().equals("X")){
         lbturno.setText(player.getNameplayer1() + " Gan√≥ X");
        state = false;
      
         return;
     }else{
        
     }
      if (lb[board.win[i][0]-1].getText().equals("O") &&
         lb[board.win[i][1]-1].getText().equals("O") &&
         lb[board.win[i][2]-1].getText().equals("O")){
         lbturno.setText( player.getNameplayer2() + " Gan√≥ O");
         state = false;
         
         return;
}
        if (lb[board.win[i][0]-1].getText().equals("") ||
             lb[board.win[i][1]-1].getText().equals("") ||
             lb[board.win[i][2]-1].getText().equals("")) {
            fullsquare = false;
        }
       
        if (state) {
             lbturno.setText("");
        }
        
   
   }
 if (fullsquare){
               lbturno.setText("Empate");
               state= false;
        }
}
